namespace Peast\Syntax\ES6\Node;

class Identifier extends Node implements Expression, Pattern
{
    protected $name;
    
    public function getName()
    {
        return $this->name;
    }
    
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }
}